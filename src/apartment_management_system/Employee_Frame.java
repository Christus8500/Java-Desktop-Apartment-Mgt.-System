    //Method To Populate The JTable With The Employee Data Using The Arraylist We Created In Our Employee_Class
    //And Also To Fill The Form With The Selected Employee's Data
    public void fillJTableWithEmployeeData(){
        Employee_Class employee = new Employee_Class();
        ArrayList<Employee_Class> employeeList = employee.employeeList();
        
        //JTable Columns
        String[] colNames = {"EmployeeID", "FirstName", "LastName", "Phone", "Email"};
        
        //JTable Rows
        //employeeList.size() = size of the arraylist
        //7 = the number of columns
        Object[][] rows = new Object[employeeList.size()][5];
        
        //add data from the list to the rows
        for(int i = 0; i < employeeList.size(); i++){
            rows[i][0] = employeeList.get(i).getId();
            rows[i][1] = employeeList.get(i).getFirstName();
            rows[i][2] = employeeList.get(i).getLastName();
            rows[i][3] = employeeList.get(i).getPhone();
            rows[i][4] = employeeList.get(i).getEmail();
            
            DefaultTableModel model = new DefaultTableModel(rows, colNames);
            jTable.setModel(model);
            
        }
        
    }
    
