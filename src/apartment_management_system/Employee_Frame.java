    private void update_employeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_employeeActionPerformed
        // TODO add your handling code here:
        //UPDATE EMPLOYEE
        String idText = id_textfield.getText();
        if (!idText.isBlank() && idText.matches("\\d+")) {
            try {
                int id = Integer.parseInt(idText);
                String firstname = fname_textfield.getText();
                String lastname = lname_textfield.getText();
                char[] passwordChars = pwd_textfield.getPassword();
                String password = new String(passwordChars);
                String phone = phone_textfield.getText();
                String email = email_textfield.getText();

                if (!firstname.isBlank() && !lastname.isBlank() && !password.isBlank() && !phone.isBlank() && !email.isBlank()) {
                    Employee_Class type = new Employee_Class(id, firstname, lastname, password, phone, email);

                    if (type.executeQueryType("edit", type)) {
                        JOptionPane.showMessageDialog(null, "Employee Updated Successfully", "Edit Employee", 1);
                    } else {
                        JOptionPane.showMessageDialog(null, "Try Again, Operation Failed", "Edit Employee", 2);
                    }
                    // Clear the text fields in any case (whether the edit operation succeeds or not).
                    clearTextFields();
                } else {
                    JOptionPane.showMessageDialog(null, "All Fields Must Not Be Empty", "Empty Field", 2);
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Invalid ID. Please enter a valid number for the ID.", "Invalid ID", 2);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Enter The ID You Wish To Update And Fill All Fields", "Empty Field", 2);
        }
        // Clear the text fields in any case (whether the edit operation succeeds or not).
        clearTextFields();

    }//GEN-LAST:event_update_employeeActionPerformed

    private void add_employeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_employeeActionPerformed
        // TODO add your handling code here:
        //ADD EMPLOYEE
        String firstname = fname_textfield.getText();
        String lastname = lname_textfield.getText();
        char[] passwordChars = pwd_textfield.getPassword();
        String password = new String(passwordChars);
        String phone = phone_textfield.getText();
        String email = email_textfield.getText();

        if (firstname.isBlank() || lastname.isBlank() || password.isBlank() || phone.isBlank() || email.isBlank()) {
            JOptionPane.showMessageDialog(null, "Fill All Fields Except The ID", "Empty Field", 2);
            // Clear the text fields using the clearTextFields method
            clearTextFields();
        } else {
            Employee_Class type = new Employee_Class(0, firstname, lastname, password, phone, email);

            if (type.executeQueryType("add", type)) {
                JOptionPane.showMessageDialog(null, "New Employee Added Successfully", "Add Employee", 1);
            } else {
                JOptionPane.showMessageDialog(null, "Try Again, Operation Failed", "Add Employee", 2);
            }

            // Clear the text fields using the clearTextFields method
            clearTextFields();
        }
    }//GEN-LAST:event_add_employeeActionPerformed

