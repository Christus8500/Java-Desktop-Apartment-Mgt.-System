    private void delete_employeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delete_employeeActionPerformed
        // TODO add your handling code here:
        //DELETE EMPLOYEE
        String idText = id_textfield.getText();
        if (!idText.isBlank() && idText.matches("\\d+")) {
            int id = Integer.parseInt(idText);
            Employee_Class type = new Employee_Class(id, "", "", "", "", "");

            int confirm = JOptionPane.showConfirmDialog(null, "Confirm Remove?", "Remove Employee", JOptionPane.YES_NO_OPTION);

            if (confirm == JOptionPane.YES_OPTION) {
                if (type.executeQueryType("remove", type)) {
                    JOptionPane.showMessageDialog(null, "Employee Removed Successfully", "Remove Employee", 1);
                } else {
                    JOptionPane.showMessageDialog(null, "Try Again, Operation Failed", "Remove Employee", 2);
                }
            }
            // Clear the text fields using the clearTextFields method
            clearTextFields();
        } else {
            JOptionPane.showMessageDialog(null, "Enter a Valid ID to Delete", "Invalid Input", 2);
        }
        
    }//GEN-LAST:event_delete_employeeActionPerformed

