    private void delete_notificationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delete_notificationActionPerformed
        // TODO add your handling code here:
        //DELETE NOTIFICATION
        String temp_name = txt_temp_name.getText();
        
        Notification_Class notification = new Notification_Class(temp_name, "", "", "");
        
        if (!temp_name.isBlank()) {
            
            int confirm = JOptionPane.showConfirmDialog(null, "Confirm Delete?", "Delete Notification", JOptionPane.YES_NO_OPTION);
            
            if (confirm == JOptionPane.YES_OPTION) {
                if(new Notification_Class().deleteNotification(notification)){
                    JOptionPane.showMessageDialog(null, "Notification Deleted Successfully", "Delete Notification", 1);
                    clearTextFields();
                }else{
                    JOptionPane.showMessageDialog(null, "Operation Failed", "Delete Notification", 2);
                }
                clearTextFields();
            }
        } else {
            JOptionPane.showMessageDialog(null, "All Fields Must Be Empty", "Empty Field", 2);
        }
        
    }//GEN-LAST:event_delete_notificationActionPerformed

