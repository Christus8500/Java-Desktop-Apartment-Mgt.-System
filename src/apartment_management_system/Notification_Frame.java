    private void update_notificationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_notificationActionPerformed
        // TODO add your handling code here:
        //EDIT NOTIFICATION
        String temp_name = txt_temp_name.getText();
        String description = txt_description.getText();
        String subject = txt_subject.getText();
        String email = txt_email_address.getText();
        
        Notification_Class notification = new Notification_Class(temp_name, description, subject, email);
        
        if (!temp_name.isBlank() && !description.isBlank() && !subject.isBlank() && !email.isBlank()) {
            if(new Notification_Class().editNotification(notification)){
                JOptionPane.showMessageDialog(null, "Notification Updated Successfully", "Update Notification", 1);
                clearTextFields();
            }else{
                JOptionPane.showMessageDialog(null, "Operation Failed", "Update Notification", 1);
            }
            clearTextFields();  
        } else {
            JOptionPane.showMessageDialog(null, "All Fields Must Be Empty", "Empty Field", 2);
        }


    }//GEN-LAST:event_update_notificationActionPerformed

